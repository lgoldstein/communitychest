{
	const DWORD	dwPos=find_string_entry(lpszColName, lpszWellKnownColorsNames,
												   (sizeof lpszWellKnownColorsNames) / sizeof(LPCTSTR),
													FALSE);
	return ((WKREFCOLORCASE) dwPos);
}

